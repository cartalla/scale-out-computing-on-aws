import os
import sqlite3

from flask import g

DATABASE = '/apps/soca/' + os.environ['CLUSTER_ID'] + '/cluster_web_ui/database.db'

def get_db():
    db = getattr(g, '_database', None)
    if db is None:
        db = g._database = sqlite3.connect(DATABASE)
    return db

@app.teardown_appcontext
def close_connection(exception):
    db = getattr(g, '_database', None)
    if db is not None:
        db.close()